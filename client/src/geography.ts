/** Hand-drawn decorative geography for the board, fitted to the stylized
 * city coordinates in shared/mapData (1000x620 viewBox). */

function poly(points: number[][]): string {
  return `M ${points.map(([x, y]) => `${x} ${y}`).join(' L ')} Z`;
}

